        <h2>Orígenes Prehispánicos</h2>
        <p>La gastronomía de San Luis Potosí tiene sus raíces en las culturas indígenas huasteca y chichimeca. Estos pueblos ancestrales basaban su alimentación en productos de la tierra como el maíz, frijoles, chiles y calabazas, conocidos como la "tríada mesoamericana". Platillos como el zacahuil, un tamal gigante envuelto en hojas de plátano, se originan en estas tradiciones, cocinados en hornos de tierra para fiestas comunitarias.</p>

        <h2>Influencia Española y Colonial</h2>
        <p>Con la llegada de los españoles en el siglo XVI, se introdujeron ingredientes como el trigo, arroz, lácteos y carnes de origen europeo. Esta fusión dio lugar a adaptaciones creativas, como las enchiladas potosinas, donde el chile rojo se combina con queso fresco y especias traídas del viejo continente. La evangelización también influyó, incorporando dulces conventuales elaborados con frutas locales y azúcar.</p>

        <h2>La Fusión Mestiza Moderna</h2>
        <p>En el siglo XIX y XX, la migración y el intercambio cultural enriquecieron aún más la cocina potosina. Hoy, eventos como la Feria Nacional Potosina celebran esta herencia mestiza, donde se fusionan sabores indígenas, españoles y hasta árabes en platillos como el asado de boda. Esta evolución refleja la identidad cultural diversa de San Luis Potosí.</p>

        <h2>Línea del Tiempo Histórica</h2>
        <ul class="timeline">
            <li><strong>Prehispánico (antes 1521):</strong> Cultivos base de maíz y chiles por huastecos y chichimecas.</li>
            <li><strong>1521-1600:</strong> Llegada española; introducción de ganado y trigo.</li>
            <li><strong>1600-1800:</strong> Desarrollo de platillos mestizos en conventos y haciendas.</li>
            <li><strong>1800-Actualidad:</strong> Consolidación de tradiciones en ferias y familias potosinas.</li>
        </ul>
